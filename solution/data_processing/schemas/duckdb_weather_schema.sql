CREATE TABLE IF NOT EXISTS weather (
    timezone VARCHAR,
    state_code INTEGER,
    country_code VARCHAR,
    lat DOUBLE,
    lon DOUBLE,
    city_name VARCHAR,
    station_id VARCHAR,
    sources VARCHAR,
    city_id INTEGER,
    rh DOUBLE,
    wind_spd DOUBLE,
    timestamp_utc VARCHAR,
    slp DOUBLE,
    vis DOUBLE,
    pod VARCHAR,
    solar_rad DOUBLE,
    pres DOUBLE,
    h_angle DOUBLE,
    dewpt DOUBLE,
    snow INTEGER,
    uv DOUBLE,
    elev_angle DOUBLE,
    wind_dir INTEGER,
    ghi DOUBLE,
    dhi DOUBLE,
    timestamp_local VARCHAR,
    dni DOUBLE,
    azimuth DOUBLE,
    datetime VARCHAR,
    temp DOUBLE,
    precip INTEGER,
    clouds INTEGER,
    ts INTEGER,
    weather_icon VARCHAR,
    weather_code INTEGER,
    weather_description VARCHAR,
    PRIMARY KEY (timestamp_local, lat, lon)
);